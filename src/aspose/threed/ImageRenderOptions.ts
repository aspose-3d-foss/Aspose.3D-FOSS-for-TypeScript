export class ImageRenderOptions {
    private _width: number = 800;
    private _height: number = 600;

    get width(): number {
        return this._width;
    }

    set width(value: number) {
        this._width = value;
    }

    get height(): number {
        return this._height;
    }

    set height(value: number) {
        this._height = value;
    }
}
